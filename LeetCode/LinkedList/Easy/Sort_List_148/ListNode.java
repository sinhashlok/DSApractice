package DSApractice.LeetCode.LinkedList.Easy.Sort_List_148;

public class ListNode {
    int val;
    ListNode next;

    ListNode () {

    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}