package DSApractice.LeetCode.LinkedList.Medium.Linked_List_Cycle_II_142;

public class ListNode {
    int val;
    ListNode next;

    ListNode () {

    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}