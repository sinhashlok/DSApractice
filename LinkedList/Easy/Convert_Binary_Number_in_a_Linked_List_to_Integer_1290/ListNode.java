package DSApractice.LeetCode.LinkedList.Easy.Convert_Binary_Number_in_a_Linked_List_to_Integer_1290;

public class ListNode {
      int val;
      ListNode next;

      ListNode() {

      }

      ListNode(int val) {
            this.val = val;
      }

      ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
      }
}